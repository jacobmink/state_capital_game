def state_game(arr):
    print("Welcome to Capital Punishment")
    for i in range(0,len(arr)):
        arr[i]["wrong"] = 0
    def game_play():
        score = 0
        random.shuffle(arr)
        sorted_list = sorted(arr, key=lambda state: state["wrong"], reverse=True)
        for i in range(0,len(sorted_list)):
            answer = input("What is the capital of {}? ".format(sorted_list[i]["name"]))
            if answer == sorted_list[i]["capital"]:
                score += 1
                print("Score: " + str(score) + " That's right!")
            else:
                print("Score: " + str(score) + " Ooh, sorry, you're an idiot")
                sorted_list[i]["wrong"] += 1
            
        else:
            replay = input("Would you like to try again? ")
            if replay == "y" or replay == "yes" or replay == "Yes":
                game_play()
    game_play()


